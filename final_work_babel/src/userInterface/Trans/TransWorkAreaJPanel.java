    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblTranslation.getModel();
        dtm.setRowCount(0);
        
        for(Enterprise e: network.getEnterpriseDirectory().getEnterpriseList()){
            for(WorkRequest wr: e.getWorkQueue().getWorkRequestList()){ 
                if(wr.getTL() == Transaccount){
                Object[] row = new Object[7];
                row[0] = wr;
                row[1] = wr.getRequestDate();
                row[2] = wr.getSR().getEmployee().getName();
                row[3] = wr.getSourceLanguage() + " → " +wr.getDestinationLanguage();
                row[4] = wr.getWordCount();
                row[5] = wr.getContentType();
                row[6] = wr.getStatus();
                dtm.addRow(row);
            }
                       long diff;
            long days=0;
            double n=0;
            DecimalFormat df = new DecimalFormat( "0.0000 "); 
            for(int i=0;i<e.getWorkQueue().getWorkRequestList().size();i++)
            {
                if(e.getWorkQueue().getWorkRequestList().get(i).getResolveDate()!=null)
                {
                    diff = e.getWorkQueue().getWorkRequestList().get(i).getResolveDate().getTime() - e.getWorkQueue().getWorkRequestList().get(i).getRequestDate().getTime();
                    days = days+diff/(60*60);
                    n=e.getWorkQueue().getWorkRequestList().get(i).getWordCount()+n;
                }
                else
                {
                    n=n+0;
                    days=days+0;
                    
                }
                
            }
            if(days==0)
            {
                TF.setText(String.valueOf(0));
            }
            else
            {
                TF.setText(String.valueOf(df.format(n/days)));
                Transaccount.setTime(Double.parseDouble(df.format(n/days)));
                System.out.println(n);
                System.out.println(days);
            }     
                
        }
        }
    }
    

        // TODO add your handling code here:
        
        int selectedRow = tblTranslation.getSelectedRow();
        if(selectedRow >= 0){
            
           
            WorkRequest wr = (WorkRequest) tblTranslation.getValueAt(selectedRow,0);
            if(wr.getStatus().equals(workStatus.Translated.name())){
                JOptionPane.showMessageDialog(null,"Work Finished");
            }else{
                wr.setStatus(workStatus.Translating.name());
                TranslatingJPanel tljp = new TranslatingJPanel(upc,Transaccount,wr);
                upc.add("TranslatingJPanel",tljp);
                CardLayout layout = (CardLayout) upc.getLayout();
                layout.next(upc);
            }
            
        }else{
            JOptionPane.showMessageDialog(null,"Please select one of your work");
        }

      

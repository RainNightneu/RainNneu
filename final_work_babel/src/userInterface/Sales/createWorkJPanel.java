    private void BtnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSubmitActionPerformed
        // TODO add your handling code here:
        
        if(txtCustomer.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Input customerName");
        }else if(txtSample.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Upload your file");
        
        }else if(cbSource.getSelectedItem().toString().equals(cbDestination.getSelectedItem().toString())){
            JOptionPane.showMessageDialog(null,"Destination Language should not be same with Source Language");
        
        }else{
        
        String custName = txtCustomer.getText();
        String articleType = cbArticleType.getSelectedItem().toString();
        String content = txtSample.getText();
        double unitPrice = (double) spinnerPrice.getValue();
        int wordCount = Integer.parseInt(txtWords.getText());
        
        String srcLanguage =  cbSource.getSelectedItem().toString();
        String desLanguage =  cbDestination.getSelectedItem().toString();
        
        String PMName = cbPM.getSelectedItem().toString();
        UserAccount PM = enterprise.getUserAccountDirectory().findUserAccountByName(PMName);
        
        WorkRequest tr =  enterprise.getWorkQueue().addTranslationRequest();
        tr.setStatus(workStatus.Received.name());
        tr.setSR(SRaccount);
        tr.setCustomerName(custName);
        tr.setContent(content);
        tr.setContentType(articleType);
        tr.setUnitPrice(unitPrice);
        tr.setWordCount(wordCount);
        tr.setSourceLanguage(srcLanguage);
        tr.setDestinationLanguage(desLanguage);
        tr.setPM(PM);
        
System.out.println(tr.getRequestDate());
        JOptionPane.showMessageDialog(null,"Success");
        upc.remove(this);
        CardLayout layout = (CardLayout) upc.getLayout();
        layout.previous(upc);


        }
    }//GEN-LAST:event_BtnSubmitActionPerformed


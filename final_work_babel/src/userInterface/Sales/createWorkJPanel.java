    public void populateCombo(){
        cbArticleType.removeAllItems();
        for(contentType type: WorkRequest.contentType.values()){
            cbArticleType.addItem(type.toString());
        }
        
        cbSource.removeAllItems();
        cbDestination.removeAllItems();
        for(Language language: WorkRequest.Language.values()){
            cbSource.addItem(language.name());
            cbDestination.addItem(language.name());
        }

        cbPM.removeAllItems();
        for(UserAccount ua: enterprise.getUserAccountDirectory().getUserAccountList()){
            if(ua.getRole().toString().equals("PMRole")){
                cbPM.addItem(ua.getEmployee().getName());
            }
        }
    }

